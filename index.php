<div class="field field-name-body field-type-text-with-summary field-label-hidden"><h2>Dirección de Servicio Social Universitario</h2>

<p>La Dirección de Servicio Social Universitario (SSU), es la unidad encargada del desarrollo de los proyectos de servicio social y voluntariado, a nivel nacional. </p>

<img id="logo" src="imag/certificados.jpg" alt="Foto de estudiantes recibiendo certificados de servicio social" />

<h2>Misión</h2>

<p>Sensibilizar y motivar al estudiante de la Universidad Tecnológica de Panamá hacia una participación activa en beneficio de las poblaciones vulnerables del país, a través del servicio social universitario y el voluntariado.</p>

<h2>Visión</h2>

<p>Ofrecer soluciones mediante la aplicación de los conocimientos científicos, tecnológicos y humanísticos de los estudiantes de la Universidad Tecnológica de Panamá, a diversos problemas de la sociedad.</p>

<h2>¿Qué es Servicio Social y el Voluntariado?</h2>

<p>El Servicio Social Universitario se define como la acción teórico-práctica que, de manera temporal y obligatoria, realizan los estudiantes de técnico y de licenciatura en beneficio de una comunidad vulnerable, como acto de reciprocidad e identificación para con ella.</p>

<p>El Voluntariado es la actividad que se realiza de manera voluntaria y desinteresada para trabajar, con fines benéficos y altruistas, en beneficio de personas o comunidades vulnerables.</p>

<p>                                 </p>

<h2>Objetivos</h2>

<ul style="text-align: left"><li>Contribuir al desarrollo tecnológico, económico y social del Estado, a través de la ejecución de proyectos de servicio social universitario y voluntariado, para satisfacer necesidades de grupos vulnerables o necesidades apremiantes de la comunidad.</li>
	<li>Desarrollar una conciencia de responsabilidad social que fomente en los estudiantes la solidaridad, ética, la participación ciudadana, la empatía y el compromiso social.</li>
	<li>Establecer una relación entre la Universidad y las comunidades como binomio impulsador de desarrollo social y cultural.</li>
</ul>


